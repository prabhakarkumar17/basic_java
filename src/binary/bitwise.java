package binary;

//BITWISE OR and AND OPERATOR

public class bitwise {

	public static void main(String[] args) 
	{
		int i=25;	//   25->11001
		int	j=26;	//   26->11010
		int k=i & j;//25&26->11000 
		int l=i | j;//25|26->11011
		System.out.println("\n bitwise OR" +l+ "AND is" +k);

	}

}
